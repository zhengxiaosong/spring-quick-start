package org.example.spring.properties.quickstart.pojo;

/**
 * Description:
 *
 * @author Song.Z
 */
public class User {
    private String id;

    private String userName;

    private String note;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}

